package com.marvelcomicsapi.objects;

public class ComicPrice {
    private String type;
    private Float price;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }
}